/**
 * Скриптовый мозг напарника — фолбэк, когда LLM недоступен
 * (нет ключа, исчерпан лимит, сбой сети).
 *
 * Принцип graceful degradation: котик НИКОГДА не молчит и не показывает
 * ошибку. Ядро петли (дробление → старт → похвала) не зависит от внешних API.
 *
 * Реплики персонализируются из памяти (план, находки, любимый час) и
 * варьируются детерминированным сидом, чтобы не повторяться подряд.
 */

import type { MemoryContextPayload } from '@/lib/companion'

export type ScriptedReply = {
  text: string
  /** Если задан — в чате рисуется карточка с кнопкой «Начинаю» */
  startStep?: string
  minutes?: number
}

// ---------- утилиты ----------

function pick<T>(arr: T[], seed: number): T {
  return arr[Math.abs(seed) % arr.length]
}

/** Обрезать пользовательский текст до короткой метки дела */
function toTaskLabel(raw: string): string {
  let cleaned = raw.replace(/[.!?…]+$/g, '').trim()
  // Срезаем служебные префиксы циклом: «не могу заставить себя начать X» → «X»
  const prefix =
    /^(мне|я|у меня|надо|нужно|не могу|никак не могу|тяжело|сложно|страшно|лень|заставить себя|заставить|начать|начинать|сесть за|сесть|взяться за|взяться|делать|сделать)\s+/i
  for (let i = 0; i < 6 && prefix.test(cleaned); i++) {
    cleaned = cleaned.replace(prefix, '').trim()
  }
  if (cleaned.length <= 60) return cleaned
  return cleaned.slice(0, 57).trimEnd() + '…'
}

function matches(text: string, patterns: RegExp[]): boolean {
  return patterns.some((p) => p.test(text))
}

// ---------- интенты ----------

const RX = {
  // ВАЖНО: \b не работает с кириллицей в JS — используем явную границу
  greeting: [/^(привет|здравствуй(те)?|хай|ку|йо|добр(ое утро|ый (день|вечер))|салют|hello|hi)([\s,.!)]|$)/i],
  cantStart: [
    /не могу (начать|заставить|сесть|взяться)/i,
    /(тяжело|сложно|страшно|лень) (начать|начинать|сесть|взяться)/i,
    /прокрастинир/i,
    /залип/i,
    /откладыва/i,
    /застрял/i,
    /не начина/i,
  ],
  feelBad: [
    /(мне|я) (плохо|устал|выгорел|тревожно|грустно|паршиво)/i,
    /выгорани/i,
    /нет сил/i,
    /депресс/i,
    /ничего не хочу/i,
  ],
  done: [
    /^(сделал|закончил|готово|начал|получилось|справился|доделал)/i,
    /я (сделал|закончил|начал|справился)/i,
  ],
  planTalk: [/завтра/i, /план/i, /распиш/i, /раздроб/i],
  island: [/остров/i, /находк/i, /что там у тебя/i, /чем занимался/i],
  whoAreYou: [/кто ты/i, /что ты (умеешь|можешь)/i, /как (это|ты) работа/i],
  thanks: [/спасибо|благодар|круто|класс|топ/i],
} as const

// ---------- генератор ----------

export function scriptedReply(
  userText: string,
  memory: MemoryContextPayload | null,
  hour: number,
): ScriptedReply {
  const text = userText.trim()
  const seed = (memory?.patterns.totalStarts ?? 0) + text.length + hour
  const isEvening = hour >= 18 || hour < 4
  const plan = memory?.plan ?? null
  const finds = memory?.recentFinds ?? []
  const lastFind = finds.length > 0 ? finds[finds.length - 1] : null

  // Благодарность / позитив
  if (matches(text, [...RX.thanks])) {
    return {
      text: pick(
        [
          'Работаем. Я тут, если что.',
          'Да ладно тебе. Просто приходи ещё.',
          'Принято. Остров тоже доволен.',
        ],
        seed,
      ),
    }
  }

  // «Сделал / начал / закончил»
  if (matches(text, [...RX.done])) {
    return {
      text: pick(
        [
          'Вот это я понимаю. Старт был — значит, день уже не ноль.',
          'Есть! Заметь: самое страшное было до начала, не после.',
          'Красавчик. Остров это тоже почувствовал.',
        ],
        seed,
      ),
    }
  }

  // «Мне плохо»
  if (matches(text, [...RX.feelBad])) {
    return {
      text: pick(
        [
          'Слышу. Сегодня не обязательно быть продуктивным — можно просто быть. Если захочешь, сделаем один смешно маленький шаг. Не захочешь — я всё равно тут.',
          'Окей, значит режим бережности. Ноль — это не минус. Хочешь, выберем что-то на две минуты, без обязательств?',
          'Понял, не давлю. Я никуда не денусь, и остров тоже. Если станет чуть легче — начнём с крошечного.',
        ],
        seed,
      ),
    }
  }

  // Про остров
  if (matches(text, [...RX.island])) {
    if (lastFind) {
      return {
        text: `Остров живёт. Последнее, что появилось — «${lastFind.name}»${
          lastFind.rarity === 'rare' ? ', между прочим редкая' : ''
        }. Каждый твой старт — что-то новое. Хочешь добавить ещё?`,
      }
    }
    return {
      text: 'Остров пока пустой берег, но это ненадолго: каждый твой старт что-то на нём выращивает. Первый старт — первый костёр.',
    }
  }

  // «Кто ты»
  if (matches(text, [...RX.whoAreYou])) {
    return {
      text: 'Я твой напарник. Помогаю начинать дела, которые не начинаются: дробим до одного крошечного действия, жмём кнопку, а на моём острове от каждого старта что-то вырастает. Что у тебя висит?',
    }
  }

  // Планирование вечером / слово «завтра»
  if (matches(text, [...RX.planTalk]) || (isEvening && plan === null && text.length < 25)) {
    return {
      text: pick(
        [
          'Давай без списков на десять пунктов. Назови одно дело на завтра — и я помогу найти его первый крошечный шаг.',
          'Окей, план на завтра. Правило одно: одно дело, один физический первый шаг. Что за дело?',
        ],
        seed,
      ),
    }
  }

  // «Не могу начать» — ядро продукта
  if (matches(text, [...RX.cantStart])) {
    const label = toTaskLabel(text)
    const step =
      plan?.firstStep ??
      (label.length > 3 && !matches(label, [...RX.cantStart])
        ? `открыть то, где живёт «${label}»`
        : 'открыть нужный файл или вкладку — и всё')
    return {
      text: pick(
        [
          'Знакомо. Ставим планку на пол: не «сделать», а просто прикоснуться к делу. Вот кнопка — 15 минут, можно уйти раньше, старт всё равно засчитается.',
          'Это не лень, это порог входа. Снижаем его до смешного: один физический шаг, и ты уже в игре. Жми.',
          'Не думай про всё дело — это ловушка. Думай про первые 15 минут. Кнопка ниже, я рядом.',
        ],
        seed,
      ),
      startStep: step,
      minutes: 15,
    }
  }

  // Приветствие
  if (matches(text, [...RX.greeting])) {
    if (plan) {
      return {
        text: `Привет! Помню: «${plan.task}», первый шаг — ${plan.firstStep.toLowerCase()}. Готов ткнуть кнопку, или сначала поговорим?`,
        startStep: plan.firstStep,
        minutes: 15,
      }
    }
    return {
      text: pick(
        [
          'Привет! Что сегодня висит и не начинается? Назови — раздробим до смешного маленького шага.',
          'Здорово. Одно дело, которое откладываешь — какое? Дальше я помогу.',
          isEvening
            ? 'Привет. Вечер — время положить план на завтра: одно дело, один первый шаг. Что за дело?'
            : 'Привет! Есть что-то, что надо было начать ещё вчера? Давай сделаем это стартом на 15 минут.',
        ],
        seed,
      ),
    }
  }

  // Дефолт: человек назвал дело — дробим и даём кнопку
  const label = toTaskLabel(text)
  if (label.length >= 3) {
    return {
      text: pick(
        [
          `Окей, «${label}». Не будем делать всё — сделаем вход: один крошечный шаг, 15 минут, уйти можно раньше.`,
          `Принял: «${label}». Правило простое — не думать про результат, думать про первое движение. Кнопка ниже.`,
          `«${label}» — записал. Самое трудное сейчас нажать кнопку, дальше легче. Проверим?`,
        ],
        seed,
      ),
      startStep: `первый шаг: ${label}`,
      minutes: 15,
    }
  }

  return {
    text: 'Расскажи, что висит — одно дело, своими словами. Дальше раздробим его до шага, который невозможно не сделать.',
  }
}

