/**
 * Имена и порядок элементов острова — единый источник правды.
 * Используется и картой острова (/world), и экраном завершения сессии
 * («на острове появилось: …»), чтобы названия никогда не расходились.
 */

export const ISLAND_ELEMENT_NAMES = [
  'Первый росток',
  'Дерево',
  'Костёр',
  'Домик',
  'Грядки',
  'Фонарь',
  'Второе дерево',
  'Флаг',
  'Причал',
  'Луна над островом',
] as const

/**
 * Что выросло после старта номер n (1-based).
 * Старты сверх карты не открывают новых элементов — остров «густеет».
 */
export function elementNameForStartNumber(n: number): string | null {
  if (n < 1 || n > ISLAND_ELEMENT_NAMES.length) return null
  return ISLAND_ELEMENT_NAMES[n - 1]
}
